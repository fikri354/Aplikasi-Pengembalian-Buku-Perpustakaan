    public void selectData(){
        String kolom[] = {"NoAnggota","NamaPeminjam","AlamatPeminjam","JenisBuku","JudulBuku","TanggalPinjam","TanggalKembali","Status"};
            DefaultTableModel dtm = new DefaultTableModel(null, kolom);
            String SQL = "SELECT * FROM t_siswa";
            ResultSet rs = KoneksiDB.executeQuery(SQL);
            try{
                while(rs.next()){
                    String NoAnggota = rs.getString(1);
                    String NamaPeminjam = rs.getString(2);
                    String AlamatPeminjam = rs.getString(3);
                    String JenisBuku = "";
                    if("".equals(rs.getString(4))){
                        JenisBuku = "Pelajaran";
                    }else{
                        JenisBuku = "Fiksi";
                    }
                    String JudulBuku = rs.getString(5);
                    String TanggalPinjam = rs.getString(6);
                    String TanggalKembali = rs.getString(7);
                    String Status = rs.getString(8);
                    
                    String data[] = {NoAnggota,NamaPeminjam,AlamatPeminjam,JenisBuku,JudulBuku,TanggalPinjam,TanggalKembali,Status};
                    dtm.addRow(data);
                }
            }catch(SQLException ex){
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            tblData.setModel(dtm);
    }
