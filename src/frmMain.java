    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
       String pinjam = dateFormat.format(tglPinjam.getDate());
       String kembali = dateFormat.format(tglKembali.getDate());
       String Status = "";
           if(rdsudah.isSelected()){
           }else{
               Status = "Sudah Kembali";
           }
           if(rdbelum.isSelected()){
               Status = "Belum Kembali";
           }
        
        if ("".equals(txtnomor.getText()) || "".equals(txtNama.getText()) ||"".equals(txtAlamat.getText()) || 
                 "".equals(cbJenis.getSelectedItem()) || "".equals(txtJudul.getText()) || "".equals(pinjam) || "".equals(kembali)){
            JOptionPane.showMessageDialog(this,"Harap Lengkapi Data!","Error",JOptionPane.WARNING_MESSAGE);
       }else{
           
           String SQL = "INSERT INTO t_siswa (NoAnggota,NamaPeminjam,AlamatPeminjam,JenisBuku,JudulBuku,TanggalPinjam,TanggalKembali,Status)"
                + "VALUES ('"+txtnomor.getText()+"','"+txtNama.getText()+"','"+txtAlamat.getText()+"','"+cbJenis.getSelectedItem()+"','"
                   +txtJudul.getText()+"','"+pinjam+"','"+kembali+"','"+Status+"')";
        int status = KoneksiDB.execute(SQL);
        if (status == 1){
            JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan","Sukses",JOptionPane.INFORMATION_MESSAGE);
            selectData();
        }else{
            JOptionPane.showMessageDialog(this,"Data Gagal Ditambahkan","Error",JOptionPane.WARNING_MESSAGE);
        }
            
        
       }
    }//GEN-LAST:event_jButton1ActionPerformed

